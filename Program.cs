// wap to print no from 1 to 10 using while loop
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp2
{
    class Program
    {
        static void Main(string[] args)
        {
            // wap to print no from 1 to 10 using while loop
            int i = 1;
            while (i <= 10)
            {
                Console.WriteLine("i = " + i);
                i++;
            }
            Console.ReadLine();

        }
    }
}

